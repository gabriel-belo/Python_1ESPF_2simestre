def calcular_area_retangulo(base=1, altura=1):
    return base * altura

print(calcular_area_retangulo())#usando os padrões
print(calcular_area_retangulo(10, 5))
print(calcular_area_retangulo(altura=5))
print(calcular_area_retangulo(base=10))#informando só a base
print(calcular_area_retangulo(altura=10, base= 5))#informando só a altura
